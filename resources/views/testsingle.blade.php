								سجل سعر أسمنت قنا والعسكري بني سويف قيمة 1350 جنيها اليوم عند البيع للمستهلك بالأسواق.
								وقال أحمد الزينى رئيس شعبة مواد البناء باتحاد الغرف التجارية، إن قرار جهاز حماية المنافسة ومنع الممارسات الاحتكارية بخفض إنتاج 23 شركة عاملة في قطاع الأسمنت عامًا آخر يُسهم في رفع سعر طن الأسمنت بالأسواق، بما يتنافى مع مصلحة المستهلك.
