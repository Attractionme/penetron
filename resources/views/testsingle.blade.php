								وطالب رئيس شعبة مواد البناء بتصدير حصة 5% على الأقل من إنتاج الشركات العاملة في قطاع الأسمنت بدلًا من خفض إنتاجها، ما يسهم في دعم موارد الدولة من العملة الأجنبية، وزيادة حجم الصادرات بما يتناسب مع رؤية الدولة «100 مليار دولار صادرات»، مبينًا أن الدولة تقوم بتدعيم صادرات الأسمنت لأفريقيا بنحو 80% من قيمة النولون البحري.
